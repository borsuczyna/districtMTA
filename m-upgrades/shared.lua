local _getVehicleUpgrades = getVehicleUpgrades

function getUpgradeName(partID)
    return partNames[partID] or 'Nieznany'
end

function getUpgradePrice(partID)
    return partPrices[partID] or 999999
end

function getUpgradeSlots()
    local keys = {}
    for key, _ in pairs(upgradesOriginalSlots) do
        table.insert(keys, key)
    end
    return keys
end

function getUpgradeSlotByModel(model)
    return upgrades[model]
end

function getVehicleOriginalUpgradeOnSlot(vehicle, name)
    local slotId = upgradesOriginalSlots[name]
    if not slotId then
        return false
    end

    local upgrade = _getVehicleUpgradeOnSlot(vehicle, slotId)
    return (upgrade and upgrade ~= 0) and upgrade or false
end

function getVehicleCustomUpgrades(vehicle)
    local parts = getElementData(vehicle, 'vehicle:custom-upgrades') or {}
    return parts
end

function getOriginalUpgradeSlotName(upgrade)
    local name = getVehicleUpgradeSlotName(upgrade)
    if name then
        return name:lower()
    end

    return false
end

function getVehicleCustomUpgradeOnSlot(vehicle, slotName)
    local parts = getVehicleCustomUpgrades(vehicle)
    return parts[slotName]
end

function getVehicleUpgradeOnSlot(vehicle, slotName)
    local upgrade = getVehicleCustomUpgradeOnSlot(vehicle, slotName)
    if not upgrade then
        upgrade = getVehicleOriginalUpgradeOnSlot(vehicle, slotName)
    end

    return upgrade
end

function getVehicleCustomCompatibleUpgrades(vehicle, slotName)
    local vehicleModel = getElementModel(vehicle)
    local componentData = componentsPositions[vehicleModel][slotName]
    if not componentData then
        return {}
    end

    local compatibleUpgrades = {}

    for _, upgrade in ipairs(publicUpgrades[slotName] or {}) do
        table.insert(compatibleUpgrades, upgrade)
    end

    local driver = getVehicleOccupant(vehicle, 0)
    if driver then
        for _, upgrade in ipairs(itemUpgrades[slotName] or {}) do
            if (localPlayer or exports['m-inventory']:doesPlayerHaveItem(driver, upgrade[1])) or doesVehicleHaveUpgrade(vehicle, upgrade[2]) then
                table.insert(compatibleUpgrades, upgrade[2])
            end
        end
    end

    return compatibleUpgrades
end

function isItemUpgrade(upgrade)
    for _, upgrades in pairs(itemUpgrades) do
        for _, itemUpgrade in ipairs(upgrades) do
            if itemUpgrade[2] == upgrade then
                return itemUpgrade[1]
            end
        end
    end

    return false
end

local function unique(array)
    local seen = {}
    local result = {}

    for _, value in ipairs(array) do
        if not seen[value] then
            table.insert(result, value)
            seen[value] = true
        end
    end

    return result
end

function getVehicleCompatibleUpgrades(vehicle, slotName)
    local slotId = upgradesOriginalSlots[slotName]
    local upgrades = {}

    if slotId then
        upgrades = _getVehicleCompatibleUpgrades(vehicle, slotId)
    end

    local customUpgrades = getVehicleCustomCompatibleUpgrades(vehicle, slotName)
    for _, upgrade in ipairs(customUpgrades) do
        table.insert(upgrades, upgrade)
    end

    return unique(upgrades)
end

function getVehicleUpgrades(vehicle)
    local upgrades = {}
    
    for slot, slotId in pairs(upgradesOriginalSlots) do
        local upgrade = _getVehicleUpgradeOnSlot(vehicle, slotId)
        if upgrade and upgrade ~= 0 then
            table.insert(upgrades, upgrade)
        end
    end

    local customUpgrades = getVehicleCustomUpgrades(vehicle)
    for _, upgrade in pairs(customUpgrades) do
        table.insert(upgrades, upgrade)
    end

    -- add mechanic tuning
    for _, upgrade in ipairs(mechanicTuning) do
        if getElementData(vehicle, upgrade) then
            table.insert(upgrades, upgrade)
        end
    end

    return upgrades
end

function doesVehicleHaveUpgrade(vehicle, upgrade)
    local upgrades = getVehicleUpgrades(vehicle)
    for _, vehicleUpgrade in ipairs(upgrades) do
        if vehicleUpgrade == upgrade then
            return true
        end
    end

    return false
end

local modelsExistsCache = {}

function doesModelExist(model)
    if not modelsExistsCache[model] then
        modelsExistsCache[model] = exports['m-models']:doesModelExist(model)
    end

    return modelsExistsCache[model]
end